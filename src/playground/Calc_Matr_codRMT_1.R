# A função matrix
# R quer que os dados entrem por coluna a partir da coluna 1.
# 1o arg: c(2,3,-2,1,2,2), os valores que irão preencher as colunas.
# 2o arg: 3, o número de linhas.
# 3o arg: 2, o número de colunas.

A <- matrix(c(2,3,-2,1,2,2),3,2)
A

# ou apenas:
A <- matrix(c(2,3,-2,1,2,2),ncol=2)
A

# para preencher a matriz pelas linhas:
A2 <- matrix(c(2,3,-2,1,2,2), 3, byrow = T)
A2

# para preencher a matriz pelas linhas:
A2 <- matrix(c(2,3,-2,1,2,2), 2, byrow = T)
A2

#########################################################################
### Quando saber se um objeto é uma matriz
#########################################################################

is.matrix(A)

is.vector(A)

